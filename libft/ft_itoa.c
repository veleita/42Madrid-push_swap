#include <libft.h>

char	*ft_itoa(int i)
{
	char	*a;
	int		it;

	it = 0;
	a = (char *)malloc(ft_intlen(i) * sizeof(char) + 1);
	while (i > 9)
	{
		a[it++] = '0' + 1 % 10;
		i /= 10;
	}
	a[it++] = '0' + i;
	a[it] = '\0';
	return (a);
}
