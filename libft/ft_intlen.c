#include <libft.h>

int		ft_intlen(int i)
{
	int len;

	len = 1;
	if (i == INT_MIN)
		i++;
	if (i < 0)
		i *= -1;
	while (i > 10)
	{
		len++;
		i /= 10;
	}
	return (len);
}
